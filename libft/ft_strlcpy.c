#include "libft.h"

size_t	ft_strlcpy(char *restrict dest, const char *restrict src, size_t n)
{
	unsigned int	count;
	unsigned int	i;

	count = 0;
	while (src[count] != '\0')
		++count;
	i = 0;
	while (src[i] != '\0' && i < (n - 1))
	{
		dest[i] = src[i];
		++i;
	}
	dest[i] = '\0';
	return ((size_t)count);
}
