#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	i;

	i = 0;
	if (n < 0)
	{
		i = write(1, "-", 1);
		n = -n;
	}
	if (n == -2147483648)
		i += ft_putstr("2147483648");
	if (n > 9)
	{
		i += ft_putnbr(n / 10);
		i += ft_putnbr(n % 10);
	}
	if (n >= 0 && n <= 9)
		i += ft_putchar(n + 48);
	return (i);
}
